      SUBROUTINE DSDBIU (B, FORM)
      INTEGER B
      INTEGER FORM
      INTEGER MEM( 1)
      COMMON/CDSMEM/MEM
      INTEGER L, S, LMAX
      INTEGER BLANKS(6)
      DATA BLANKS(1)/9/,BLANKS(2)/32/,BLANKS(3)/32/,BLANKS(4)/32/,BLANKS
     *(5)/32/,BLANKS(6)/-2/
      CALL PUTINT (B, 5, 2)
      CALL PUTCH (32, 2)
      CALL PUTINT (MEM (B + 0), 0, 2)
      CALL REMARK (14H words in use.)
      L = 0
      S = B + MEM (B + 0)
      IF (.NOT.(FORM .EQ. 48))GOTO 23000
      LMAX = 5
      GOTO 23001
23000 CONTINUE
      LMAX = 50
23001 CONTINUE
      B = B + 2
23002 IF (.NOT.(B .LT. S))GOTO 23004
      IF (.NOT.(L .EQ. 0))GOTO 23005
      CALL PUTLIN (BLANKS, 2)
23005 CONTINUE
      IF (.NOT.(FORM .EQ. 48))GOTO 23007
      CALL PUTINT (MEM (B), 10, 2)
      GOTO 23008
23007 CONTINUE
      IF (.NOT.(FORM .EQ. 97))GOTO 23009
      CALL PUTCH (MEM (B), 2)
23009 CONTINUE
23008 CONTINUE
      L = L + 1
      IF (.NOT.(L .GE. LMAX))GOTO 23011
      L = 0
      CALL PUTCH (10, 2)
23011 CONTINUE
23003 B = B + 1
      GOTO 23002
23004 CONTINUE
      IF (.NOT.(L .NE. 0))GOTO 23013
      CALL PUTCH (10, 2)
23013 CONTINUE
      RETURN
      END

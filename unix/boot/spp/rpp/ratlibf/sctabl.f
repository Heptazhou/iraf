      INTEGER SYM (100)
      INTEGER INFO (100)
      INTEGER MEM( 1)
      COMMON/CDSMEM/MEM
      INTEGER BUCKET, WALKER
      INTEGER DSGET
      INTEGER NODSIZ, I, J
      IF (.NOT.(POSN .EQ. 0))GOTO 23000
      POSN = DSGET (2)
      MEM (POSN) = 1
      MEM (POSN + 1) = MEM (TABLE + 1)
23000 CONTINUE
      BUCKET = MEM (POSN)
      WALKER = MEM (POSN + 1)
      NODSIZ = MEM (TABLE)
23002 CONTINUE
      IF (.NOT.(WALKER .NE. 0))GOTO 23005
      I = WALKER + 1 + NODSIZ
      J = 1
23007 IF (.NOT.(MEM (I) .NE. -2))GOTO 23008
      SYM (J) = MEM (I)
      I = I + 1
      J = J + 1
      GOTO 23007
23008 CONTINUE
      SYM (J) = -2
      I = 1
23009 IF (.NOT.(I .LE. NODSIZ))GOTO 23011
      J = WALKER + 1 + I - 1
      INFO (I) = MEM (J)
23010 I = I + 1
      GOTO 23009
23011 CONTINUE
      MEM (POSN) = BUCKET
      MEM (POSN + 1) = MEM (WALKER + 0)
      SCTABL = 1
      RETURN
23005 CONTINUE
      BUCKET = BUCKET + 1
      IF (.NOT.(BUCKET .GT. 43))GOTO 23012
      GOTO 23004
23012 CONTINUE
      J = TABLE + BUCKET
      WALKER = MEM (J)
23006 CONTINUE
23003 GOTO 23002
23004 CONTINUE
      CALL DSFREE (POSN)
      POSN = 0
      SCTABL = -1
      RETURN
      END

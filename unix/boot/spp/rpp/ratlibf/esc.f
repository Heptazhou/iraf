      INTEGER FUNCTION ESC (ARRAY, I)
      INTEGER ARRAY (100)
      INTEGER I
      IF (.NOT.(ARRAY (I) .NE. 64))GOTO 23000
      ESC = ARRAY (I)
      GOTO 23001
23000 CONTINUE
      IF (.NOT.(ARRAY (I+1) .EQ. -2))GOTO 23002
      ESC = 64
      GOTO 23003
23002 CONTINUE
      I = I + 1
      IF (.NOT.(ARRAY (I) .EQ. 110 .OR. ARRAY (I) .EQ. 78))GOTO 23004
      ESC = 10
      GOTO 23005
23004 CONTINUE
      IF (.NOT.(ARRAY (I) .EQ. 116 .OR. ARRAY (I) .EQ. 84))GOTO 23006
      ESC = 9
      GOTO 23007
23006 CONTINUE
      ESC = ARRAY (I)
23007 CONTINUE
23005 CONTINUE
23003 CONTINUE
23001 CONTINUE
      RETURN
      END

      SUBROUTINE QUERY (MESG)
      INTEGER MESG (100)
      INTEGER GETARG
      INTEGER ARG1 (3), ARG2 (1)
      IF (.NOT.(GETARG (1, ARG1, 3) .NE. -1 .AND. GETARG (2, ARG2, 1) .E
     *Q. -1))GOTO 23000
      IF (.NOT.(ARG1 (1) .EQ. 63 .AND. ARG1 (2) .EQ. -2))GOTO 23002
      CALL ERROR (MESG)
23002 CONTINUE
23000 CONTINUE
      RETURN
      END

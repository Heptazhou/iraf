      INTEGER FUNCTION PATSIZ (PAT, N)
      INTEGER PAT (128)
      INTEGER N
      IF (.NOT.(PAT (N) .EQ. 97 .OR. PAT (N) .EQ. 123 .OR. PAT (N) .EQ. 
     *125))GOTO 23000
      PATSIZ = 2
      GOTO 23001
23000 CONTINUE
      IF (.NOT.(PAT (N) .EQ. 37 .OR. PAT (N) .EQ. 36 .OR. PAT (N) .EQ. 6
     *3))GOTO 23002
      PATSIZ = 1
      GOTO 23003
23002 CONTINUE
      IF (.NOT.(PAT (N) .EQ. 91 .OR. PAT (N) .EQ. 110))GOTO 23004
      PATSIZ = PAT (N + 1) + 2
      GOTO 23005
23004 CONTINUE
      IF (.NOT.(PAT (N) .EQ. 42))GOTO 23006
      PATSIZ = 4
      GOTO 23007
23006 CONTINUE
      CALL ERROR (24Hin patsiz: can't happen.)
23007 CONTINUE
23005 CONTINUE
23003 CONTINUE
23001 CONTINUE
      RETURN
      END

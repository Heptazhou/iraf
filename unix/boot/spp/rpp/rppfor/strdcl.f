      SUBROUTINE STRDCL
      COMMON /CDEFIO/ BP, BUF (4096)
      INTEGER BP
      INTEGER BUF
      COMMON /CFNAME/ FCNAME (30)
      INTEGER FCNAME
      COMMON /CFOR/ FORDEP, FORSTK (200)
      INTEGER FORDEP
      INTEGER FORSTK
      COMMON /CGOTO/ XFER
      INTEGER XFER
      COMMON /CLABEL/ LABEL, RETLAB, MEMFLG, COL, LOGIC0
      INTEGER LABEL
      INTEGER RETLAB
      INTEGER MEMFLG
      INTEGER COL
      INTEGER LOGIC0
      COMMON /CLINE/ LEVEL, LINECT (5), INFILE (5), FNAMP, FNAMES ( 150)
      INTEGER LEVEL
      INTEGER LINECT
      INTEGER INFILE
      INTEGER FNAMP
      INTEGER FNAMES
      COMMON /CMACRO/ CP, EP, EVALST (500), DEFTBL
      INTEGER CP
      INTEGER EP
      INTEGER EVALST
      INTEGER DEFTBL
      COMMON /COUTLN/ OUTP, OUTBUF (74)
      INTEGER OUTP
      INTEGER OUTBUF
      COMMON /CSBUF/ SBP, SBUF(2048), SMEM(240)
      INTEGER SBP
      INTEGER SBUF
      INTEGER SMEM
      COMMON /CSWTCH/ SWTOP, SWLAST, SWSTAK(1000), SWVNUM, SWVLEV, SWVST
     *K(10), SWINRG
      INTEGER SWTOP
      INTEGER SWLAST
      INTEGER SWSTAK
      INTEGER SWVNUM
      INTEGER SWVLEV
      INTEGER SWVSTK
      INTEGER SWINRG
      COMMON /CKWORD/ RKWTBL
      INTEGER RKWTBL
      COMMON /CLNAME/ FKWTBL, NAMTBL, GENTBL, ERRTBL, XPPTBL
      INTEGER FKWTBL
      INTEGER NAMTBL
      INTEGER GENTBL
      INTEGER ERRTBL
      INTEGER XPPTBL
      COMMON /ERCHEK/ ERNAME, BODY, ESP, ERRSTK(30)
      INTEGER ERNAME
      INTEGER BODY
      INTEGER ESP
      INTEGER ERRSTK
      INTEGER MEM( 60000)
      COMMON/CDSMEM/MEM
      INTEGER T, TOKEN (100), DCHAR (100)
      INTEGER GNBTOK
      INTEGER I, J, K, N, LEN
      INTEGER LENGTH, CTOI, LEX
      INTEGER CHAR(11)
      INTEGER DAT(6)
      INTEGER EOSS(3)
      DATA CHAR(1)/105/,CHAR(2)/110/,CHAR(3)/116/,CHAR(4)/101/,CHAR(5)/1
     *03/,CHAR(6)/101/,CHAR(7)/114/,CHAR(8)/42/,CHAR(9)/50/,CHAR(10)/47/
     *,CHAR(11)/-2/
      DATA DAT(1)/100/,DAT(2)/97/,DAT(3)/116/,DAT(4)/97/,DAT(5)/32/,DAT(
     *6)/-2/
      DATA EOSS(1)/48/,EOSS(2)/47/,EOSS(3)/-2/
      T = GNBTOK (TOKEN, 100)
      IF (.NOT.(T .NE. -9))GOTO 23000
      CALL SYNERR (21Hmissing string token.)
23000 CONTINUE
      CALL SQUASH (TOKEN)
      CALL OUTTAB
      CALL PBSTR (CHAR)
23002 CONTINUE
      T = GNBTOK (DCHAR, 100)
      IF (.NOT.(T .EQ. 47))GOTO 23005
      GOTO 23004
23005 CONTINUE
      CALL OUTSTR (DCHAR)
23003 GOTO 23002
23004 CONTINUE
      CALL OUTCH (32)
      CALL OUTSTR (TOKEN)
      CALL ADDSTR (TOKEN, SBUF, SBP, 2048)
      CALL ADDCHR (-2, SBUF, SBP, 2048)
      IF (.NOT.(GNBTOK (TOKEN, 100) .NE. 40))GOTO 23007
      LEN = LENGTH (TOKEN) + 1
      IF (.NOT.(TOKEN (1) .EQ. 39 .OR. TOKEN (1) .EQ. 34))GOTO 23009
      LEN = LEN - 2
23009 CONTINUE
      GOTO 23008
23007 CONTINUE
      T = GNBTOK (TOKEN, 100)
      I = 1
      LEN = CTOI (TOKEN, I)
      IF (.NOT.(TOKEN (I) .NE. -2))GOTO 23011
      CALL SYNERR (20Hinvalid string size.)
23011 CONTINUE
      IF (.NOT.(GNBTOK (TOKEN, 100) .NE. 41))GOTO 23013
      CALL SYNERR (20Hmissing right paren.)
      GOTO 23014
23013 CONTINUE
      T = GNBTOK (TOKEN, 100)
23014 CONTINUE
23008 CONTINUE
      CALL OUTCH (40)
      CALL OUTNUM (LEN)
      CALL OUTCH (41)
      CALL OUTDON
      IF (.NOT.(TOKEN (1) .EQ. 39 .OR. TOKEN (1) .EQ. 34))GOTO 23015
      LEN = LENGTH (TOKEN)
      TOKEN (LEN) = -2
      CALL ADDSTR (TOKEN (2), SBUF, SBP, 2048)
      GOTO 23016
23015 CONTINUE
      CALL ADDSTR (TOKEN, SBUF, SBP, 2048)
23016 CONTINUE
      CALL ADDCHR (-2, SBUF, SBP, 2048)
      T = LEX (TOKEN)
      CALL PBSTR (TOKEN)
      IF (.NOT.(T .NE. -75))GOTO 23017
      I = 1
23019 IF (.NOT.(I .LT. SBP))GOTO 23021
      CALL OUTTAB
      CALL OUTSTR (DAT)
      K = 1
      J = I + LENGTH (SBUF (I)) + 1
23022 CONTINUE
      IF (.NOT.(K .GT. 1))GOTO 23025
      CALL OUTCH (44)
23025 CONTINUE
      CALL OUTSTR (SBUF (I))
      CALL OUTCH (40)
      CALL OUTNUM (K)
      CALL OUTCH (41)
      CALL OUTCH (47)
      IF (.NOT.(SBUF (J) .EQ. -2))GOTO 23027
      GOTO 23024
23027 CONTINUE
      N = SBUF (J)
      CALL OUTNUM (N)
      CALL OUTCH (47)
      K = K + 1
23023 J = J + 1
      GOTO 23022
23024 CONTINUE
      CALL PBSTR (EOSS)
23029 CONTINUE
      T = GNBTOK (TOKEN, 100)
      CALL OUTSTR (TOKEN)
23030 IF (.NOT.(T .EQ. 47))GOTO 23029
23031 CONTINUE
      CALL OUTDON
23020 I = J + 1
      GOTO 23019
23021 CONTINUE
      SBP = 1
23017 CONTINUE
      RETURN
      END
C     LOGIC0  LOGICAL_COLUMN

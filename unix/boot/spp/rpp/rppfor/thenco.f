      INTEGER ERNAME
      INTEGER BODY
      INTEGER ESP
      INTEGER ERRSTK
      INTEGER MEM( 60000)
      COMMON/CDSMEM/MEM
      INTEGER SIFERR(20)
      INTEGER SIFNO0(15)
      DATA SIFERR(1)/105/,SIFERR(2)/102/,SIFERR(3)/32/,SIFERR(4)/40/,SIF
     *ERR(5)/46/,SIFERR(6)/110/,SIFERR(7)/111/,SIFERR(8)/116/,SIFERR(9)/
     *46/,SIFERR(10)/120/,SIFERR(11)/101/,SIFERR(12)/114/,SIFERR(13)/112

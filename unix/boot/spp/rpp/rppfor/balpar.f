      SUBROUTINE BALPAR
      INTEGER T, TOKEN (100)
      INTEGER GETTOK, GNBTOK
      INTEGER NLPAR
      IF (.NOT.(GNBTOK (TOKEN, 100) .NE. 40))GOTO 23000
      CALL SYNERR (19Hmissing left paren.)
      RETURN
23000 CONTINUE
      CALL OUTSTR (TOKEN)
      NLPAR = 1
23002 CONTINUE
      T = GETTOK (TOKEN, 100)
      IF (.NOT.(T .EQ. 59 .OR. T .EQ. 123 .OR. T .EQ. 125 .OR. T .EQ. -1
     *))GOTO 23005
      CALL PBSTR (TOKEN)
      GOTO 23004
23005 CONTINUE
      IF (.NOT.(T .EQ. 10))GOTO 23007
      TOKEN (1) = -2
      GOTO 23008
23007 CONTINUE
      IF (.NOT.(T .EQ. 40))GOTO 23009
      NLPAR = NLPAR + 1
      GOTO 23010
23009 CONTINUE
      IF (.NOT.(T .EQ. 41))GOTO 23011
      NLPAR = NLPAR - 1
23011 CONTINUE
23010 CONTINUE
23008 CONTINUE
      IF (.NOT.(T .EQ. -9))GOTO 23013
      CALL SQUASH (TOKEN)
23013 CONTINUE
      CALL OUTSTR (TOKEN)
23003 IF (.NOT.(NLPAR .LE. 0))GOTO 23002
23004 CONTINUE
      IF (.NOT.(NLPAR .NE. 0))GOTO 23015
      CALL SYNERR (33Hmissing parenthesis in condition.)
23015 CONTINUE
      RETURN
      END
